a = float(input('Insira o comprimento da piscina: '))
b = float(input('Insira o comprimento da piscina: '))
c = float(input('Insira o comprimento da piscina: '))
print('As dimensões da piscina são: ',a,'x',b,'x',c)
vm = a*b*c
print('O volume da Piscina em m³ é: ',vm)
vl = vm*1000
print('O volume da Piscina em L é: ',vl)
cus = 0.0025 *vl
print('O custo para encher a piscina foi de: ',cus)



